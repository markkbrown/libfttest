#include "libft.h"

int		ft_isprime(int n)
{
	int		fct;

	fct = 2;
	if (n < 2)
		return (0);
	if (n == 2 || n == 3)
		return (1);
	while (n % fct++ != 0)
	{
		if (fct == (n - 1))
			return (1);
	}
	return (0);
}
