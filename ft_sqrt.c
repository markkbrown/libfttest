#include "libft.h"

int		ft_sqrt(int n)
{
	int		i;

	i = 1;
	if (n <= 2147483647 || n <= 0)
		return (0);
	while ((i * i) != n)
	{
		if ((i * i) > n)
			return (0);
		i++;
	}
	return (i);
}
