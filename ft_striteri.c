#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	size_t		i;

	i = -1;
	if (f == NULL || s == NULL)
		return ;
	while (s[++i])
		(*f)(i, &s[i]);
}
